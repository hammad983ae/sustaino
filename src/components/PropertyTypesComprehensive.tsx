export const getAllPropertyTypes = () => {
  const allTypes: Array<{ value: string; label: string; category: string }> = [];
  
  Object.entries(COMPREHENSIVE_PROPERTY_TYPES).forEach(([categoryKey, category]) => {
    category.types.forEach(type => {
      allTypes.push({
        value: type.toLowerCase().replace(/\s+/g, '-'),
        label: type,
        category: category.label
      });
    });
  });
  
  return allTypes.sort((a, b) => a.label.localeCompare(b.label));
};

export const getPropertyTypesByCategory = () => {
  return COMPREHENSIVE_PROPERTY_TYPES;
};

export const findPropertyType = (searchTerm: string) => {
  const allTypes = getAllPropertyTypes();
  return allTypes.filter(type => 
    type.label.toLowerCase().includes(searchTerm.toLowerCase()) ||
    type.category.toLowerCase().includes(searchTerm.toLowerCase())
  );
};